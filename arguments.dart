// Here num1 and num2 are parameters
void add(int num1, int num2) {
  int sum;
  sum = num1 + num2;

  print("The sum is $sum");
}

void main() {
// Here 10 and 20 are arguments
  add(10, 20);
}

//Parameters --> we pass the datatype as well as variable name (1 or multiple).
//Arguments  --> we pass the values of particular method.
